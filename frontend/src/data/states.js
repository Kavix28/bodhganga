export const getStateById = (id) => {
    return indianStates.find(state => state.id === id);
};

export const getStatesByName = (searchTerm) => {
    const term = searchTerm.toLowerCase();
    return indianStates.filter(state =>
        state.name.toLowerCase().includes(term) ||
        state.code.toLowerCase().includes(term)
    );
};

export const getTotalStatesCount = () => indianStates.length;
